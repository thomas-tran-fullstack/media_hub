  function translate(lang){
    const base = translations[lang] || translations['en'];
    const extra = extraTranslations[lang] || extraTranslations['en'] || {};
    const map = Object.assign({}, base, extra);
    // elements with data-i18n attribute use that key
    document.querySelectorAll('[data-i18n]').forEach(el=>{
      const k = el.getAttribute('data-i18n');
      if (map[k]) {
        // Use innerHTML if content contains HTML markup, otherwise textContent
        if (map[k].includes('<')) {
          el.innerHTML = map[k];
        } else {
          el.textContent = map[k];
        }
      }
    });

    // selector map
    selectorMap.forEach(m=>{
      document.querySelectorAll(m.sel).forEach(el=>{
        if (map[m.key]) el.textContent = map[m.key];
      });
    });
    // placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el=>{
      const key = el.getAttribute('data-i18n-placeholder');
      if (map[key]) el.setAttribute('placeholder', map[key]);
    });
    // titles
    document.querySelectorAll('[data-i18n-title]').forEach(el=>{
      const key = el.getAttribute('data-i18n-title');
      if (map[key]) el.setAttribute('title', map[key]);
    });
  }

  function init(){
    const saved = localStorage.getItem('preferred_lang') || (navigator.language && navigator.language.split('-')[0]) || 'vi';
    const lang = saved in translations ? saved : 'en';
    translate(lang);

    // If there's a language select on page, wire it
    const languageSelect = document.getElementById('languageSelect');
    if (languageSelect){
      languageSelect.value = lang;
      languageSelect.addEventListener('change', (e)=>{
        const v = e.target.value;
        localStorage.setItem('preferred_lang', v);
        translate(v);
        // also trigger any page specific timezone population if available
        if (typeof window.onLanguageChanged === 'function') window.onLanguageChanged(v);
      });
    }
  }

  // Expose a simple helper to get a single translation key at runtime
  window.t = function(key){
    const lang = localStorage.getItem('preferred_lang') || (navigator.language && navigator.language.split('-')[0]) || 'vi';
    const base = translations[lang] || translations['en'];
    const extra = extraTranslations[lang] || extraTranslations['en'] || {};
    const map = Object.assign({}, base, extra);
    return map[key] || key;
  };

